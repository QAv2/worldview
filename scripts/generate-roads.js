function interpolateRoute(waypoints, maxSegLen) {
  // Add intermediate points if waypoints are far apart
  const result = [waypoints[0]];
  for (let i = 1; i < waypoints.length; i++) {
    const [lon1, lat1] = waypoints[i - 1];
    const [lon2, lat2] = waypoints[i];
    const dist = Math.sqrt((lon2 - lon1) ** 2 + (lat2 - lat1) ** 2);
    const steps = Math.max(1, Math.ceil(dist / maxSegLen));
    for (let s = 1; s <= steps; s++) {
      const t = s / steps;
      result.push([
        Math.round((lon1 + (lon2 - lon1) * t) * 1000) / 1000,
        Math.round((lat1 + (lat2 - lat1) * t) * 1000) / 1000,
      ]);
    }
  }
  return result;
}

function main() {
  console.log('Generating curated road data for WorldView traffic layer...\n');

  const segments = [];
  for (const [name, waypoints] of Object.entries(ROUTES)) {
    if (waypoints.length < 2) continue;
    // Interpolate to smooth out long segments (max ~1° between points)
    const interpolated = interpolateRoute(waypoints, 1.0);
    segments.push(interpolated);
  }

  // Also split longer routes into sub-segments for more particle coverage
  const finalSegments = [];
  for (const seg of segments) {
    if (seg.length > 12) {
      // Split into chunks of ~8 points with 2-point overlap
      for (let i = 0; i < seg.length - 1; i += 6) {
        const chunk = seg.slice(i, i + 8);
        if (chunk.length >= 2) finalSegments.push(chunk);
      }
    } else {
      finalSegments.push(seg);
    }
  }

  console.log(`Routes defined: ${Object.keys(ROUTES).length}`);
  console.log(`Segments after splitting: ${finalSegments.length}`);

  const json = JSON.stringify(finalSegments);
  fs.writeFileSync(OUTPUT, json);
  const sizeKB = (Buffer.byteLength(json) / 1024).toFixed(1);
  console.log(`Written to ${OUTPUT} (${sizeKB} KB)`);
}

main();
